	@CORR_SUSCRIPCION_DESTINO INT,
	@CORR_CONFI_PAIS_DESTINO INT,
	@CORR_EMPRESA_DESTINO INT)
AS
	DECLARE @CON_CATALOGO_CUENTA TABLE
	(
		CUENTA_CONTABLE varchar(30)
