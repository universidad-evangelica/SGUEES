	--WHERE SUBSTRING(cuenta_contable,1,1)=@CODIGO_RUBRO
	--AND id_empresa=@CORR_EMPRESA_ORIGEN

	WHILE EXISTS(SELECT 1 FROM @CON_CATALOGO_CUENTA)
	BEGIN		
		SELECT TOP 1 @CUENTA_CONTABLE=CUENTA_CONTABLE 
		FROM @CON_CATALOGO_CUENTA 
		ORDER BY CUENTA_CONTABLE		

