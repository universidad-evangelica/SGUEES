				--AND TARGET.CORR_CONFI_PAIS=SOURCE.CORR_CONFI_PAIS
				--AND TARGET.CORR_EMPRESA=SOURCE.CORR_EMPRESA
				--AND TARGET.ANIO_PERIODO=SOURCE.ANIO_PERIODO
				--AND TARGET.MES_PERIODO=SOURCE.MES_PERIODO
				--AND TARGET.CORR_DOCUMENTO=SOURCE.CORR_DOCUMENTO				
