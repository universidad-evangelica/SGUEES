CREATE PROCEDURE [dbo].[PRAL_GENE_COM_DOCUMENTO_TOTAL_IMPUESTO]
(
	@CORR_EMPRESA INT,
	@ANIO_PERIODO INT,
	@MES_PERIODO INT,
	@CORR_DOCUMENTO INT OUTPUT,
	@SYS_LOGIN_USUARIO VARCHAR(30),
	@SYS_ESTACION VARCHAR(50),
	@SYS_FILAS_AFECTADAS INT OUTPUT,
	@SYS_NUMERO_ERROR NUMERIC(38,0) OUTPUT,
	@SYS_MENSAJE_ERROR NVARCHAR(4000) OUTPUT
)
AS
BEGIN
	
	SET NOCOUNT ON

	SELECT @SYS_NUMERO_ERROR=0,@SYS_MENSAJE_ERROR='',@SYS_FILAS_AFECTADAS=0
	DECLARE @CLASE_DOCUMENTO CHAR(5)
	, @CORR_TIPO_DOC INT
	, @ESTADO_DOCUMENTO VARCHAR(2)
	, @PORC_PERCEPCION DECIMAL(14,2)
	, @PORC_RETENCION DECIMAL(14,2)
	, @NATURAL_JURIDICO VARCHAR(1)
	, @CLASE_RUBRO VARCHAR(3)
	, @MONTO_GRAVADO_SIN_IVA DECIMAL(18,2)
	, @MONTO_RUBRO_TOTAL DECIMAL(18,2)
	
	BEGIN TRY 
		SELECT @ESTADO_DOCUMENTO=ESTADO_DOCUMENTO, @CLASE_DOCUMENTO=CLASE_DOCUMENTO, @NATURAL_JURIDICO=B.TIPO_PERSONERIA
		FROM V_COM_DOCUMENTO A
		INNER JOIN V_COM_PROVEEDOR B
		ON A.CORR_PROVEEDOR=B.CORR_PROVEEDOR 
		WHERE A.CORR_EMPRESA=@CORR_EMPRESA
		AND A.ANIO_PERIODO=@ANIO_PERIODO
		AND A.MES_PERIODO=@MES_PERIODO
		AND A.CORR_DOCUMENTO=@CORR_DOCUMENTO
		
		IF @ESTADO_DOCUMENTO = 'DI'
		BEGIN
			IF @CLASE_DOCUMENTO IN ('FAC', 'CCF', 'FEX','NCR','CRT', 'INT', 'IMP', 'EXC', 'ESP','DCLE') 
			BEGIN
				EXECUTE PRAL_CALC_COM_PORC_RETE_PERC
					   @CORR_EMPRESA
					  ,@ANIO_PERIODO
					  ,@MES_PERIODO
					  ,@CORR_DOCUMENTO
					  ,@PORC_RETENCION OUTPUT
					  ,@PORC_PERCEPCION OUTPUT
					  ,0 
					  ,0 
					  
				DELETE A FROM COM_DOCUMENTO_TOTAL A
				INNER JOIN GEN_RUBRO B ON A.CORR_EMPRESA=B.CORR_EMPRESA  AND A.CORR_RUBRO=B.CORR_RUBRO 
				WHERE A.CORR_EMPRESA=@CORR_EMPRESA 
				AND A.ANIO_PERIODO=@ANIO_PERIODO 
				AND A.MES_PERIODO=@MES_PERIODO 
				AND A.CORR_DOCUMENTO=@CORR_DOCUMENTO 
				AND B.ES_IMPUESTO=1
					
				
				--IMPUESTOS AL RUBRO GRAVADO
				MERGE COM_DOCUMENTO_TOTAL AS TARGET
				USING ( SELECT DISTINCT A.CORR_EMPRESA,A.ANIO_PERIODO,A.MES_PERIODO,
						A.CORR_DOCUMENTO,C.CORR_IMPUESTO, 0 MONTO_RUBRO, C.IMPUESTO_INCLUIDO
						--CASE WHEN C.IMPUESTO_INCLUIDO=1 THEN (A.MONTO_RUBRO)-((A.MONTO_RUBRO)/(1+(D.POR_IMPUESTO/100.00)))*D.SUMA_RESTA 
						--ELSE A.MONTO_RUBRO*(D.POR_IMPUESTO/100.00)*D.SUMA_RESTA END AS MONTO_RUBRO
						FROM V_COM_DOCUMENTO_TOTAL A
						INNER JOIN GEN_RUBRO B ON A.CORR_EMPRESA=B.CORR_EMPRESA AND A.CORR_RUBRO=B.CORR_RUBRO 
						INNER JOIN GEN_RUBRO_IMPUESTO C ON B.CORR_EMPRESA=C.CORR_EMPRESA AND B.CORR_RUBRO=C.CORR_RUBRO 
						INNER JOIN GEN_RUBRO D ON C.CORR_EMPRESA=D.CORR_EMPRESA AND C.CORR_IMPUESTO=D.CORR_RUBRO 
						--INNER JOIN GEN_EMPRESA E ON A.CORR_EMPRESA = E.CORR_EMPRESA
						--INNER JOIN V_GEN_PAIS F ON E.CORR_SUSCRIPCION = F.CORR_SUSCRIPCION AND E.CORR_PAIS = F.CORR_PAIS
						WHERE A.CORR_EMPRESA=@CORR_EMPRESA
						AND A.ANIO_PERIODO=@ANIO_PERIODO
						AND A.MES_PERIODO=@MES_PERIODO
						AND A.CORR_DOCUMENTO=@CORR_DOCUMENTO
						AND B.ES_IMPUESTO<>1
						AND D.ES_IMPUESTO=1
						AND (D.CLASE_RUBRO NOT IN ('RET', 'PER', 'ISR') OR (D.CLASE_RUBRO='RET' AND @PORC_RETENCION<>0) OR (D.CLASE_RUBRO='PER' AND @PORC_PERCEPCION<>0) OR (D.CLASE_RUBRO='ISR' AND (@NATURAL_JURIDICO='N')))-- OR ISNULL(F.CODIGO_PAIS,'') NOT IN('ES','222'))))
						) AS SOURCE
				ON TARGET.CORR_EMPRESA=SOURCE.CORR_EMPRESA
				AND TARGET.ANIO_PERIODO=SOURCE.ANIO_PERIODO
				AND TARGET.MES_PERIODO=SOURCE.MES_PERIODO
				AND TARGET.CORR_DOCUMENTO=SOURCE.CORR_DOCUMENTO
				AND TARGET.CORR_RUBRO=SOURCE.CORR_IMPUESTO
				WHEN MATCHED 
					AND TARGET.CORR_EMPRESA=@CORR_EMPRESA 
					AND TARGET.ANIO_PERIODO=@ANIO_PERIODO 
					AND TARGET.MES_PERIODO=@MES_PERIODO 
					AND TARGET.CORR_DOCUMENTO=@CORR_DOCUMENTO 
					THEN UPDATE SET TARGET.MONTO_RUBRO=SOURCE.MONTO_RUBRO, TARGET.IMPUESTO_INCLUIDO=SOURCE.IMPUESTO_INCLUIDO
				WHEN NOT MATCHED BY TARGET 
					THEN INSERT (CORR_EMPRESA,ANIO_PERIODO,MES_PERIODO,
					CORR_DOCUMENTO,CORR_RUBRO,MONTO_RUBRO,IMPUESTO_INCLUIDO)
					VALUES (SOURCE.CORR_EMPRESA,SOURCE.ANIO_PERIODO,SOURCE.MES_PERIODO,
					SOURCE.CORR_DOCUMENTO,SOURCE.CORR_IMPUESTO,SOURCE.MONTO_RUBRO,SOURCE.IMPUESTO_INCLUIDO);				
				
				--IMPUESTOS ESPECIALES AL TIPO DE GASTO
				MERGE COM_DOCUMENTO_TOTAL AS TARGET
				USING ( SELECT A.CORR_EMPRESA,A.ANIO_PERIODO,A.MES_PERIODO,
						A.CORR_DOCUMENTO,C.CORR_RUBRO CORR_IMPUESTO, 0 MONTO_RUBRO, C.IMPUESTO_INCLUIDO
						--CASE WHEN C.IMPUESTO_INCLUIDO=1 THEN (A.MONTO_RUBRO)-((A.MONTO_RUBRO)/(1+(D.POR_IMPUESTO/100.00)))*D.SUMA_RESTA 
						--ELSE A.MONTO_RUBRO*(D.POR_IMPUESTO/100.00)*D.SUMA_RESTA END AS MONTO_RUBRO
						FROM V_COM_DOCUMENTO A
						INNER JOIN GEN_TIPO_GASTO B ON A.CORR_EMPRESA=B.CORR_EMPRESA AND A.CORR_TIPO_GASTO=B.CORR_TIPO_GASTO
						INNER JOIN GEN_TIPO_GASTO_IMPUESTO C ON B.CORR_EMPRESA=C.CORR_EMPRESA AND B.CORR_TIPO_GASTO=C.CORR_TIPO_GASTO 
						INNER JOIN GEN_RUBRO D ON C.CORR_EMPRESA=D.CORR_EMPRESA AND C.CORR_RUBRO=D.CORR_RUBRO 
						WHERE A.CORR_EMPRESA=@CORR_EMPRESA
						AND A.ANIO_PERIODO=@ANIO_PERIODO
						AND A.MES_PERIODO=@MES_PERIODO
						AND A.CORR_DOCUMENTO=@CORR_DOCUMENTO
						AND D.ES_IMPUESTO=1
						AND (D.CLASE_RUBRO NOT IN ('RET', 'PER', 'ISR') OR (D.CLASE_RUBRO='RET' AND @PORC_RETENCION<>0) OR (D.CLASE_RUBRO='PER' AND @PORC_PERCEPCION<>0) OR (D.CLASE_RUBRO='ISR' AND @NATURAL_JURIDICO='N'))
						) AS SOURCE
				ON TARGET.CORR_EMPRESA=SOURCE.CORR_EMPRESA
				AND TARGET.ANIO_PERIODO=SOURCE.ANIO_PERIODO
				AND TARGET.MES_PERIODO=SOURCE.MES_PERIODO
				AND TARGET.CORR_DOCUMENTO=SOURCE.CORR_DOCUMENTO
				AND TARGET.CORR_RUBRO=SOURCE.CORR_IMPUESTO
