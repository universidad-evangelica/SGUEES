						FROM (SELECT  ISNULL(A.CORR_RUBRO,0) CORR_RUBRO,ISNULL(A.MONTO_RUBRO,0)/(1.0000+SUM(CASE WHEN B.IMPUESTO_INCLUIDO=1 THEN ((C.POR_IMPUESTO*E.SUMA_RESTA)/100.0000) ELSE 0.0000 END))	MONTO_SIN_IMPUESTOS_INCLUIDOS
							FROM COM_DOCUMENTO_TOTAL A
							INNER JOIN GEN_RUBRO_IMPUESTO B ON  A.CORR_EMPRESA=B.CORR_EMPRESA AND A.CORR_RUBRO=B.CORR_RUBRO 
							INNER JOIN V_COM_DOCUMENTO_TOTAL C ON B.CORR_EMPRESA=C.CORR_EMPRESA AND B.CORR_IMPUESTO=C.CORR_RUBRO
							INNER JOIN GEN_RUBRO_IMPUESTO D ON  A.CORR_EMPRESA=D.CORR_EMPRESA AND A.CORR_RUBRO=D.CORR_RUBRO 
							INNER JOIN GEN_RUBRO E ON C.CORR_EMPRESA=E.CORR_EMPRESA AND C.CORR_RUBRO=E.CORR_RUBRO 
							WHERE A.CORR_EMPRESA=@CORR_EMPRESA 						 
							AND A.ANIO_PERIODO=@ANIO_PERIODO 
							AND A.MES_PERIODO=@MES_PERIODO 
							AND A.CORR_DOCUMENTO=@CORR_DOCUMENTO 
							AND C.ANIO_PERIODO=@ANIO_PERIODO 
							AND C.MES_PERIODO=@MES_PERIODO 
							AND C.CORR_DOCUMENTO=@CORR_DOCUMENTO 
							AND D.CORR_IMPUESTO=@CORR_IMPUESTO
							AND C.ES_IMPUESTO=1
							AND C.TIPO_APLICACION='PVG'
							GROUP BY ISNULL(A.CORR_RUBRO,0),ISNULL(A.MONTO_RUBRO,0)
						) AA												
						
						--IF ISNULL(@MONTO_SIN_IMPUESTOS_INCLUIDOS,0)<=0 --Si el monto gravado es cero verifico si esta como impuesto especial en el item
						--BEGIN																			
