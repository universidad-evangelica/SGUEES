				--UPDATE SET TARGET.MONTO_RUBRO=SOURCE.MONTO_RUBRO, TARGET.IMPUESTO_INCLUIDO=SOURCE.IMPUESTO_INCLUIDO, TARGET.ORDEN_TOTAL=SOURCE.ORDEN_TOTAL
				--WHEN NOT MATCHED BY TARGET 
				--	THEN INSERT (CORR_SUSCRIPCION,CORR_CONFI_PAIS,CORR_EMPRESA,ANIO_PERIODO,MES_PERIODO,
				--	CORR_DOCUMENTO,CORR_RUBRO,MONTO_RUBRO,IMPUESTO_INCLUIDO,ORDEN_TOTAL)
				--	VALUES (SOURCE.CORR_SUSCRIPCION,SOURCE.CORR_CONFI_PAIS,SOURCE.CORR_EMPRESA,SOURCE.ANIO_PERIODO,SOURCE.MES_PERIODO,
				--	SOURCE.CORR_DOCUMENTO,SOURCE.CORR_IMPUESTO,SOURCE.MONTO_RUBRO,SOURCE.IMPUESTO_INCLUIDO,SOURCE.ORDEN_TOTAL);
