						--		INNER JOIN INV_ITEM_IMPUESTO C B.CORR_EMPRESA=C.CORR_EMPRESA AND B.CORR_TIPO_ITEM=C.CORR_TIPO_ITEM AND B.CORR_ITEM=C.CORR_ITEM 
						--		INNER JOIN GEN_RUBRO E ON C.CORR_EMPRESA=E.CORR_EMPRESA AND C.CORR_RUBRO=E.CORR_RUBRO 															
