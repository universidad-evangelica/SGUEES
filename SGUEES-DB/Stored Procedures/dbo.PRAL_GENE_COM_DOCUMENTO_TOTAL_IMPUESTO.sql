				DECLARE @CONTADOR INT, @CANTIDAD_REGISTROS INT, @CORR_IMPUESTO INT, @IMPUESTO_INCLUIDO BIT, @TIPO_APLICACION VARCHAR(3)
				
				INSERT INTO @TEMP1 
				SELECT CORR_RUBRO AS CORR_IMPUESTO, IMPUESTO_INCLUIDO, TIPO_APLICACION, 0 ESTA_PROC
				FROM V_COM_DOCUMENTO_TOTAL  
				WHERE CORR_EMPRESA=@CORR_EMPRESA 
				AND ANIO_PERIODO=@ANIO_PERIODO 
				AND MES_PERIODO=@MES_PERIODO 
				AND CORR_DOCUMENTO=@CORR_DOCUMENTO 
				AND ES_IMPUESTO=1
				
				SELECT @CONTADOR=0, @CANTIDAD_REGISTROS=COUNT(*)
				FROM @TEMP1 
				
				WHILE (@CONTADOR<@CANTIDAD_REGISTROS )
				BEGIN
					SELECT TOP 1 @CORR_IMPUESTO=CORR_IMPUESTO, @IMPUESTO_INCLUIDO=IMPUESTO_INCLUIDO, @TIPO_APLICACION=TIPO_APLICACION 
					FROM @TEMP1 WHERE ESTA_PROC=0
					
					IF @TIPO_APLICACION='PVG' --PORCENTAJE VALOR GRAVADO
					BEGIN
						DECLARE @MONTO_SIN_IMPUESTOS_INCLUIDOS DECIMAL(12,4)
												
