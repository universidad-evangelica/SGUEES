CREATE TRIGGER [ti_CON_RUBRO_NIVEL] ON [dbo].[CON_RUBRO_NIVEL] 
FOR INSERT
AS
	DECLARE
	@numrows  int,
	@numnull  int,
	@errno    int,
	@errmsg   varchar(255),
	@CORR_EMPRESA smallint,
	@CODIGO_RUBRO varchar(30),
	@NUMERO_CARACTERES tinyint,
	@NIVEL int,
	@TAMANO_CUENTA int
BEGIN

	SELECT  @numrows = @@rowcount
	IF @numrows = 0
	RETURN

	IF @numrows > 1
	BEGIN
		SELECT @errno  = 30000,
		@errmsg = 'No se Puede Insertar Mas de un Registro a la Vez'
		GOTO ERROR
	END

	SELECT @CORR_EMPRESA=CORR_EMPRESA,
		@CODIGO_RUBRO=CODIGO_RUBRO,
		@NUMERO_CARACTERES=NUMERO_CARACTERES,
		@NIVEL=NIVEL,
		@TAMANO_CUENTA=DATALENGTH(CODIGO_RUBRO)
	FROM inserted

	IF (@NIVEL > 1)
	BEGIN
		IF NOT EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL
                 WHERE CODIGO_RUBRO=@CODIGO_RUBRO
                 AND NIVEL=@NIVEL-1
                 AND CORR_EMPRESA=@CORR_EMPRESA)
		BEGIN
			SELECT @errno  = 30020,
			@errmsg = 'No Existe Nivel de Rubro Anterior a Nivel '+LTRIM(RTRIM(STR(@NIVEL)))
			GOTO ERROR
		END

		IF EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL A
					INNER JOIN inserted B ON  A.CORR_EMPRESA=B.CORR_EMPRESA AND A.CODIGO_RUBRO=B.CODIGO_RUBRO
					WHERE A.NIVEL<>B.NIVEL
					AND A.CODIGO_RUBRO=@CODIGO_RUBRO
					AND A.NUMERO_CARACTERES=@NUMERO_CARACTERES             
					)
		BEGIN
			SELECT @errno  = 30021,
			@errmsg = 'Ya Existe Nivel de Rubro con Numero de Carateres '+LTRIM(RTRIM(STR(@NUMERO_CARACTERES)))
			GOTO ERROR
		END
	END

	DECLARE DETA_RUBR CURSOR
	FOR SELECT CODIGO_RUBRO
		FROM CON_RUBRO
		WHERE CODIGO_RUBRO<>@CODIGO_RUBRO
		AND CORR_EMPRESA=@CORR_EMPRESA
	
	OPEN DETA_RUBR
	FETCH DETA_RUBR INTO @CODIGO_RUBRO
	WHILE (@@FETCH_STATUS=0)
	BEGIN
		IF NOT EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL
         	    WHERE CORR_EMPRESA=@CORR_EMPRESA
        	    AND CODIGO_RUBRO=@CODIGO_RUBRO
        	    AND NIVEL=@NIVEL)
		BEGIN
			INSERT INTO CON_RUBRO_NIVEL
			(CORR_EMPRESA,CODIGO_RUBRO,NIVEL,NUMERO_CARACTERES)
			VALUES(@CORR_EMPRESA,@CODIGO_RUBRO,@NIVEL,@NUMERO_CARACTERES)
		END
		FETCH DETA_RUBR INTO @CODIGO_RUBRO
	END
	CLOSE DETA_RUBR
	DEALLOCATE DETA_RUBR

RETURN

/*  Errors handling  */

ERROR:
	RAISERROR (@errmsg, 16, 1)
	ROLLBACK TRANSACTION
END
