  [CODIGO_RUBRO] [varchar](30) NOT NULL,
  [NIVEL] [smallint] NOT NULL,
  [NUMERO_CARACTERES] [smallint] NOT NULL,
  CONSTRAINT [PK_CON_RUBRO_NIVEL] PRIMARY KEY CLUSTERED ([CORR_EMPRESA], [CODIGO_RUBRO], [NIVEL])
)
ON [PRIMARY]
GO

