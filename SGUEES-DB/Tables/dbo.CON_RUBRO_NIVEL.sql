CREATE TRIGGER [td_CON_RUBRO_NIVEL] ON [dbo].[CON_RUBRO_NIVEL] 
FOR DELETE
AS
	DECLARE
	@numrows  int,
	@numnull  int,
	@errno    int,
	@errmsg   varchar(255),
	@CORR_EMPRESA smallint,
	@CODIGO_RUBRO varchar(30),
	@NIVEL int,
	@NUMERO_CARACTERES int,
	@TAMANO_CUENTA int
BEGIN

	SELECT  @numrows = @@rowcount
	IF @numrows = 0
	RETURN

	IF @numrows > 1
	BEGIN
		SELECT @errno  = 30002,
		@errmsg = 'No se Puede Eliminar Mas de un Registro a la Vez'
		GOTO ERROR
	END

	SELECT @CORR_EMPRESA=CORR_EMPRESA,
		@CODIGO_RUBRO=CODIGO_RUBRO,
		@NUMERO_CARACTERES=NUMERO_CARACTERES,
		@TAMANO_CUENTA=DATALENGTH(CODIGO_RUBRO),
		@NIVEL=NIVEL
	FROM deleted

	IF EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL
			WHERE CODIGO_RUBRO=@CODIGO_RUBRO
			AND NIVEL>@NIVEL
			AND CORR_EMPRESA=@CORR_EMPRESA)
	BEGIN
		SELECT @errno  = 30020,
		@errmsg = 'Existe Nivel de Rubro Posterior a Nivel '+LTRIM(RTRIM(str(@NIVEL)))
		GOTO ERROR
	END

	IF EXISTS(SELECT 1 FROM CON_CATALOGO_CUENTA
			WHERE SUBSTRING(CUENTA_CONTABLE,1,@TAMANO_CUENTA)=@CODIGO_RUBRO
			AND DATALENGTH(CUENTA_CONTABLE)=@NUMERO_CARACTERES 
			AND CORR_EMPRESA=@CORR_EMPRESA)
	BEGIN
		SELECT @errno  = 30010,
		@errmsg = 'Existe(n) Cuenta(s) en Catalogo Contable definida(s) con largo de cuenta de '+LTRIM(RTRIM(str(@NUMERO_CARACTERES)))+' caracter(es)'
		GOTO ERROR
	END

	DECLARE DETA_RUBR CURSOR
	FOR SELECT CODIGO_RUBRO
	FROM CON_RUBRO
	WHERE CODIGO_RUBRO<>@CODIGO_RUBRO
	AND CORR_EMPRESA=@CORR_EMPRESA

	OPEN DETA_RUBR 
	FETCH DETA_RUBR INTO @CODIGO_RUBRO
	WHILE (@@FETCH_STATUS=0)
	BEGIN
		IF EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL
         	    WHERE CORR_EMPRESA=@CORR_EMPRESA
        	    AND CODIGO_RUBRO=@CODIGO_RUBRO
        	    AND NIVEL=@NIVEL)
		BEGIN
			DELETE FROM CON_RUBRO_NIVEL
			WHERE CORR_EMPRESA=@CORR_EMPRESA
			AND CODIGO_RUBRO=@CODIGO_RUBRO
			AND NIVEL=@NIVEL
		END          
		FETCH DETA_RUBR INTO @CODIGO_RUBRO
	END
	CLOSE DETA_RUBR
	DEALLOCATE DETA_RUBR

RETURN

/*  Errors handling  */

ERROR:
	RAISERROR (@errmsg, 16, 1)
	ROLLBACK TRANSACTION
END
