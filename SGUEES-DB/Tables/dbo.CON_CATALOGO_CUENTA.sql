CREATE TRIGGER [tu_CON_CATALOGO_CUENTA] ON [dbo].[CON_CATALOGO_CUENTA] 
FOR UPDATE 
AS
	DECLARE
	@numrows  int,
	@numnull  int,
	@errno    int,
	@errmsg   varchar(255),
	@CORR_EMPRESA INT,	
	@CUENTA_CONTABLE varchar(30),
	@NUMERO_CARACTERES int,
	@NIVEL int,	
	@ES_DETALLE BIT
BEGIN
	SELECT  @numrows = @@rowcount
	IF @numrows = 0
	RETURN

	IF @numrows > 1
	BEGIN
		SELECT @errno  = 30000,
		@errmsg = 'No se Puede Actualizar Más de un Registro a la Vez'
		GOTO ERROR
	END

	SELECT @CORR_EMPRESA=CORR_EMPRESA,
	@ES_DETALLE=ES_DETALLE,
	@CUENTA_CONTABLE=CUENTA_CONTABLE,
	@NIVEL=NIVEL
	FROM inserted
	
	--Evaluando que no se ponga como general una cuenta con movimientos y una de detalle con subcuentas
	IF UPDATE(ES_DETALLE)
	BEGIN
		IF @ES_DETALLE=0
		BEGIN
			IF EXISTS(SELECT 1 FROM CON_PARTIDA_DETA 
					WHERE CORR_EMPRESA=@CORR_EMPRESA
					AND CUENTA_CONTABLE=@CUENTA_CONTABLE)
			BEGIN
				SELECT @errno  = 30050,
				@errmsg = 'Existen Comprobantes Contables no puede ser General'
				GOTO ERROR
			END
		END ELSE
		BEGIN			
			--Determinar si hay subcuentas
			IF EXISTS(SELECT 1 FROM CON_CATALOGO_CUENTA
			  WHERE CORR_EMPRESA=@CORR_EMPRESA
			  AND SUBSTRING(CUENTA_CONTABLE,1,@NUMERO_CARACTERES)=@CUENTA_CONTABLE
			  AND NIVEL>@NIVEL)
			BEGIN
				SELECT @errno  = 30050,
				@errmsg = 'Existen SubCuentas no Puede Ser de Detalle'
				GOTO ERROR
			END			
		END						
	END		

RETURN

/*  ERRORs HANDLING  */

ERROR:
	RAISERROR (@errmsg, 16, 1)
	ROLLBACK TRANSACTION
END
