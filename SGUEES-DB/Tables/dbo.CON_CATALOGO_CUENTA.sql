CREATE TRIGGER [ti_CON_CATALOGO_CUENTA] ON [dbo].[CON_CATALOGO_CUENTA]
FOR INSERT
AS
	DECLARE
	@numrows  int,
	@numnull  int,
	@errno    int,
	@errmsg   varchar(255),
	@CORR_EMPRESA int,
	@CODIGO_RUBRO varchar(30),
	@CUENTA_CONTABLE varchar(20),
	@NUMERO_CARACTERES int,
	@NIVEL int,
	@CUENTA_MAYOR varchar(20),
	@NUMERO_CARACTERES_MAYOR int,
	@ES_DEBE bit,
	@ES_HABER bit	
BEGIN
	SELECT  @numrows = @@rowcount
	IF @numrows = 0
	RETURN

	IF @numrows > 1
	BEGIN
		SELECT @errno  = 30000,
		@errmsg = 'No se Puede Insertar Mas de un Registro a la Vez'
		GOTO ERROR
	END

	SELECT @CORR_EMPRESA=CORR_EMPRESA,
	@CODIGO_RUBRO=SUBSTRING(CUENTA_CONTABLE,1,1),
	@CUENTA_CONTABLE=CUENTA_CONTABLE,
	@NUMERO_CARACTERES=LEN(CUENTA_CONTABLE)		
	FROM inserted
	
	IF NOT EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL 
				WHERE CODIGO_RUBRO=@CODIGO_RUBRO
				AND NUMERO_CARACTERES=@NUMERO_CARACTERES 
				AND CORR_EMPRESA=@CORR_EMPRESA)
	BEGIN
		SELECT @errno  = 30020,
		@errmsg = 'NO Existe un Nivel Para la Cantidad de Caracteres de la Cuenta. Verifique el Tamano de la Cuenta'
		GOTO ERROR
	END 
		
	SELECT @NIVEL=NIVEL
	FROM CON_RUBRO_NIVEL
	WHERE CODIGO_RUBRO=@CODIGO_RUBRO
	AND NUMERO_CARACTERES=@NUMERO_CARACTERES
	AND CORR_EMPRESA=@CORR_EMPRESA
		
	IF (@NIVEL > 1)
	BEGIN
		IF NOT EXISTS(SELECT 1 FROM CON_RUBRO_NIVEL
							WHERE CODIGO_RUBRO=@CODIGO_RUBRO
							AND NIVEL=@NIVEL-1 
							AND CORR_EMPRESA=@CORR_EMPRESA
					  )
		BEGIN
			SELECT @errno  = 30030,
			@errmsg = 'ERROR en Tabla Niveles por Rubro. No existe Nivel Anterior'
			GOTO ERROR
		END
		
		SELECT @NUMERO_CARACTERES_MAYOR=A.NUMERO_CARACTERES
		FROM CON_RUBRO_NIVEL A
		WHERE A.CORR_EMPRESA=@CORR_EMPRESA
		AND A.CODIGO_RUBRO=@CODIGO_RUBRO
		AND A.NIVEL=@NIVEL-1			
		
		--Obteniendo cuenta de mayor
		IF NOT EXISTS(SELECT 1 FROM CON_CATALOGO_CUENTA A
				  WHERE A.CORR_EMPRESA=@CORR_EMPRESA		  
				  AND A.CUENTA_CONTABLE=SUBSTRING(@CUENTA_CONTABLE,1,@NUMERO_CARACTERES_MAYOR))				  
				  
		BEGIN
			SELECT @errno  = 30030,
			@errmsg = 'No Existe cuenta de Mayor.'
			GOTO ERROR
		END
		
		SELECT @CUENTA_MAYOR=CUENTA_CONTABLE
		FROM CON_CATALOGO_CUENTA A
		WHERE A.CORR_EMPRESA=@CORR_EMPRESA				  
		AND A.CUENTA_CONTABLE=SUBSTRING(@CUENTA_CONTABLE,1,@NUMERO_CARACTERES_MAYOR)
		
		--Verificando que la cuenta de mayor NO sea de detalle
		IF EXISTS(SELECT 1 FROM CON_CATALOGO_CUENTA
				  WHERE CORR_EMPRESA=@CORR_EMPRESA
				  AND CUENTA_CONTABLE=@CUENTA_MAYOR
				  AND ISNULL(ES_DETALLE,0)=1)
		BEGIN
			SELECT @errno  = 30030,
			@errmsg = 'Cuenta de Mayor es de Detalle'
			GOTO ERROR
		END				
	END	

RETURN

/*  ERRORS HANDLING  */

ERROR:
	RAISERROR (@errmsg, 16, 1)
	ROLLBACK TRANSACTION
END
