CREATE TRIGGER [td_CON_CATALOGO_CUENTA] ON [dbo].[CON_CATALOGO_CUENTA] 
FOR DELETE
AS
	DECLARE
	@numrows  int,
	@numnull  int,
	@errno    int,
	@errmsg   varchar(255),
	@CORR_EMPRESA int,
	@CODIGO_RUBRO varchar(30),
	@CUENTA_CONTABLE varchar(30),
	@NUMERO_CARACTERES int,
	@NIVEL int
BEGIN
	SELECT  @numrows = @@rowcount
	IF @numrows = 0
	RETURN

	IF @numrows > 1
	BEGIN
		SELECT @errno  = 30000,
		@errmsg = 'No se Puede Eliminar Mas de un Registro a la Vez'
		GOTO ERROR
	END
	
	SELECT @CORR_EMPRESA=CORR_EMPRESA,
	@CODIGO_RUBRO=SUBSTRING(A.CUENTA_CONTABLE,1,1),
	@CUENTA_CONTABLE=CUENTA_CONTABLE,
	@NUMERO_CARACTERES=DATALENGTH(A.CUENTA_CONTABLE),	
	@NIVEL=NIVEL
	FROM deleted A			
	
	--Determinar si hay subcuentas
	IF EXISTS(SELECT 1 FROM CON_CATALOGO_CUENTA
			  WHERE CORR_EMPRESA=@CORR_EMPRESA
			  AND SUBSTRING(CUENTA_CONTABLE,1,@NUMERO_CARACTERES)=@CUENTA_CONTABLE
			  AND NIVEL>@NIVEL)
	BEGIN
		SELECT @errno  = 30050,
		@errmsg = 'Existen SubCuentas no Puede Eliminarse'
		GOTO ERROR
	END
	
	--Eliminar la cuenta de la tabla de historicos
	DELETE FROM CON_CATALOGO_SALDO_HIS
	WHERE CORR_EMPRESA=@CORR_EMPRESA
	AND CUENTA_CONTABLE=@CUENTA_CONTABLE
	
	--Eliminar la cuenta del saldo
	DELETE FROM CON_CATALOGO_SALDO
	WHERE CORR_EMPRESA=@CORR_EMPRESA
	AND CUENTA_CONTABLE=@CUENTA_CONTABLE
	
RETURN

/*  ERRORs hANDling  */

ERROR:
	RAISERROR (@errmsg, 16, 1)
	ROLLBACK TRANSACTION
END
