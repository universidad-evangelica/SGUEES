CREATE VIEW [dbo].[V_COM_BANCO]
AS
SELECT A.CORR_BANCO
      ,A.NOMBRE_BANCO
      ,A.NOMBRE_BANCO_CORTO
      ,A.CLASE_BANCO
	  ,CASE A.CLASE_BANCO
	  WHEN 'CREDO'  THEN 'Credomatic'
	  WHEN 'CUSCA'  THEN 'Cuscatlan'
	  WHEN 'BAG'  THEN 'Agricola'
	  WHEN 'BDV'  THEN 'Davivienda'
	  WHEN 'BPM'  THEN 'Promerica'END	NOMBRE_CLASE_BANCO
      ,A.CODIGO_TRANSACION_UNI
FROM COM_BANCO A
GO
